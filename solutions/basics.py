x = 2
y = 10

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)

# You can also assign the result to variables like so:
z = x + y
print(z)

z = x - y
print(z)

z = x * y
print(z)

z = x / y
print(z)

z = x % y
print(z)
